package entidades;

public class servicosExecutados {
	int idServicosExecutados;
	String nomeServico;
	String descricaoServico;
	float valorServico;
	
	public int getIdServicosExecutados() {
		return idServicosExecutados;
	}
	public void setIdServicosExecutados(int idServicosExecutados) {
		this.idServicosExecutados = idServicosExecutados;
	}
	public String getNomeServico() {
		return nomeServico;
	}
	public void setNomeServico(String nomeServico) {
		this.nomeServico = nomeServico;
	}
	public String getDescricaoServico() {
		return descricaoServico;
	}
	public void setDescricaoServico(String descricaoServico) {
		this.descricaoServico = descricaoServico;
	}
	public float getValorServico() {
		return valorServico;
	}
	public void setValorServico(float valorServico) {
		this.valorServico = valorServico;
	}
	
}
